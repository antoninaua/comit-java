package org.comit.course._02_operators;

public class Assignment {

	public static void main(String[] args) {
		
		int a = 4;
		
		a = a + 5;
		a+=5; // the same as a = a + 5;
		
		a = a - 5;
		a-=5; //the same as a = a - 5;
		
		a = a * 5;
		a*=5;//the same as a = a * 5;
		
		a = a / 5;
		a/=5;//the same as a = a / 5;

	}

}
